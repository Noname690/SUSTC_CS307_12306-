package function;

public class oder {
        int oder_id;
        int price;
        String date;
        String train_num;
        String start_station;
        String stop_station;
        String seat_type;

        public int getPrice() {
                return price;
        }

        public int getOder_id() {
                return oder_id;
        }

        public String getDate() {
                return date;
        }

        public String getStart_station() {
                return start_station;
        }

        public String getStop_station() {
                return stop_station;
        }

        public String getSeat_type() {
                return seat_type;
        }

        public String getTrain_num() {
                return train_num;
        }
}
